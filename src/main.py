    logger = logging.getLogger(__name__)
    logger.info(f"–õ–æ–≥–∏—Ä–æ–≤–∞–Ω–∏–µ –Ω–∞—Å—Ç—Ä–æ–µ–Ω–æ (—É—Ä–æ–≤–µ–Ω—å: {LOG_LEVEL})")
    
